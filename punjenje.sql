--MAPRAVI DATUME
INSERT INTO KS55324_DIM_VREME (
    DATUM,
    DAN,
    MESEC,
    NAZIV_MESECA,
    KVARTAL,
    GODINA
)
SELECT
    d AS DATUM,
    EXTRACT(DAY FROM d) AS DAN,
    EXTRACT(MONTH FROM d) AS MESEC,
    TRIM(TO_CHAR(d, 'MONTH')) AS NAZIV_MESECA,
    TO_NUMBER(TO_CHAR(d, 'Q')) AS KVARTAL,
    EXTRACT(YEAR FROM d) AS GODINA
FROM (
    SELECT DATE '2010-01-01' + LEVEL - 1 AS d
    FROM dual
    CONNECT BY LEVEL <= (DATE '2030-12-31' - DATE '2010-01-01' + 1)
);

COMMIT;

