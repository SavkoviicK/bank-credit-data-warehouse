--NOVI GRADOVI

INSERT INTO KS55324_GRAD (GRAD_ID, NAZIV, POST_BROJ, REGION_ID)
SELECT 5 + ROW_NUMBER() OVER (ORDER BY s.city, r.REGION_ID) AS GRAD_ID,
       s.city,
       NULL AS POST_BROJ,
       r.REGION_ID
FROM (
  SELECT DISTINCT TRIM(city) AS city,
                  TRIM(country) AS country,
                  TRIM(admin_name) AS admin_name
  FROM KS55324_STG_WORLDCITIES
  WHERE city IS NOT NULL
    AND country IS NOT NULL
    AND admin_name IS NOT NULL
) s
JOIN KS55324_DRZAVA d
  ON TRIM(d.NAZIV) = s.country
JOIN KS55324_REGION r
  ON r.DRZAVA_ID = d.DRZAVA_ID
 AND TRIM(r.NAZIV) = s.admin_name
WHERE NOT EXISTS (
  SELECT 1
  FROM KS55324_GRAD g
  WHERE TRIM(g.NAZIV) = s.city
    AND g.REGION_ID = r.REGION_ID
);

COMMIT;

SELECT COUNT(*) FROM KS55324_GRAD;

