LEFT JOIN KS55324_GRAD g ON g.GRAD_ID = p.GRAD_ID
ORDER BY p.POSLOVNICA_ID;

-- NOVI REGIONI
INSERT INTO KS55324_REGION (REGION_ID, NAZIV, DRZAVA_ID)
SELECT 5 + ROW_NUMBER() OVER (ORDER BY d.DRZAVA_ID, s.admin_name) AS REGION_ID,
       s.admin_name,
       d.DRZAVA_ID
FROM (
  SELECT DISTINCT TRIM(country) AS country,
                  TRIM(admin_name) AS admin_name
  FROM KS55324_STG_WORLDCITIES
  WHERE country IS NOT NULL
    AND admin_name IS NOT NULL
) s
JOIN KS55324_DRZAVA d
  ON TRIM(d.NAZIV) = s.country
WHERE NOT EXISTS (
  SELECT 1
  FROM KS55324_REGION r
  WHERE TRIM(r.NAZIV) = s.admin_name
    AND r.DRZAVA_ID = d.DRZAVA_ID
);

COMMIT;

