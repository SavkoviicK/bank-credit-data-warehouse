INSERT INTO KS55324_DRZAVA (DRZAVA_ID, NAZIV)
SELECT 5 + ROW_NUMBER() OVER (ORDER BY country) AS DRZAVA_ID,
       country
FROM (
  SELECT DISTINCT TRIM(country) AS country
  FROM KS55324_STG_WORLDCITIES
  WHERE country IS NOT NULL
    AND TRIM(country) NOT IN (
      'Srbija',
      'Crna Gora',
      'Bosna',
      'Hrvatska',
      'Afghanistan'
    )
);

COMMIT;

